package com.uzh.ase.dailygrind.pushnotificationsservice.pushNotification.repository.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import com.uzh.ase.dailygrind.pushnotificationsservice.pushNotification.controller.dto.SubscriptionDto;
import lombok.*;

@DynamoDBTable(tableName = "push-subscriptions")
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class PushSubscription {

    @DynamoDBAutoGeneratedKey
    @DynamoDBHashKey(attributeName = "subscription_id")
    private String subscriptionId;

    @DynamoDBAttribute(attributeName = "user_id")
    private String userId;

    @DynamoDBAttribute(attributeName = "push_subscription")
    private SubscriptionDto pushSubscription;
}
